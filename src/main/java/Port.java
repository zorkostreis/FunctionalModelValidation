public class Port {
    private int id;
    private PortTypes type;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public PortTypes getType() {
        return type;
    }

    public void setType(PortTypes type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
